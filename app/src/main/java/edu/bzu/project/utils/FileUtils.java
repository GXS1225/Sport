    /**将图片保存到本地时 进行压缩
     * 
     * 即bitmap-->file形式
     * 特点： File形式的图片确实被压缩了, 但是当你重新读取压缩后的file为 Bitmap是,它占用的内存并没有改变
     * */
	public static void saveBitmap(Bitmap bm, String picName) {
		try {
			if (!isFileExist("")) {
				File tempf = createSDDir("");
			}
			File f = new File(SDPATH, picName + ".JPEG"); //创建图片文件
			if (f.exists()) {//存在此图片文件则删除之
				f.delete();
			}
			FileOutputStream out = new FileOutputStream(f);//获取图片的输出流
			bm.compress(Bitmap.CompressFormat.JPEG, 70, out);//压缩一下，但是在取得时候，bitmap的大小不会改变
			out.flush();
			out.close();
		} catch (FileNotFoundException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
	}

